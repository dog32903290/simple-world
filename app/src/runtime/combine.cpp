bool inSelection(const std::vector<int>& ids, int childId) {
  return std::find(ids.begin(), ids.end(), childId) != ids.end();
}

// The selected children's bounding box (positions are node top-lefts; fine for centering).
void selectionBounds(const Symbol& parent, const std::vector<int>& ids, float& minX,
                     float& minY, float& maxX, float& maxY) {
  bool first = true;
  for (const SymbolChild& c : parent.children) {
    if (!inSelection(ids, c.id)) continue;
    if (first) { minX = maxX = c.x; minY = maxY = c.y; first = false; continue; }
    minX = std::min(minX, c.x); maxX = std::max(maxX, c.x);
    minY = std::min(minY, c.y); maxY = std::max(maxY, c.y);
  }
}

// A slot's def on the SYMBOL a child references (the lib carries atomics too — they are
// imported on AddChild / regenerated by the loader). nullptr when unresolvable.
const SlotDef* slotDefOf(const SymbolLibrary& lib, const Symbol& parent, int childId,
                         const std::string& slotId, bool isInput) {
  const SymbolChild* c = childById(parent, childId);
  const Symbol* s = c ? lib.find(c->symbolId) : nullptr;
  if (!s) return nullptr;
  const std::vector<SlotDef>& defs = isInput ? s->inputDefs : s->outputDefs;
  for (const SlotDef& d : defs)
    if (d.id == slotId) return &d;
  return nullptr;
}

// Unique slot id within the new symbol: the base name, then base1, base2, ... (TiXL's
// duplicate-name counter, Combine.cs:56-58).
std::string uniqueSlotId(const std::string& base, const std::vector<SlotDef>& ins,
                         const std::vector<SlotDef>& outs) {
  auto taken = [&](const std::string& id) {
    for (const SlotDef& d : ins)
      if (d.id == id) return true;
    for (const SlotDef& d : outs)
      if (d.id == id) return true;
    return false;
  };
  if (!taken(base)) return base;
  for (int n = 1;; ++n) {
    std::string cand = base + std::to_string(n);
    if (!taken(cand)) return cand;
  }
}

